# üß† –ó–∞–≤–¥–∞–Ω–Ω—è. –ü–ª–∞–Ω—É–≤–∞–Ω–Ω—è –≤–∏—Ä–æ–±–Ω–∏—Ü—Ç–≤–∞ –∑ –æ–±–º–µ–∂–µ–Ω–Ω—è–º –ø–æ—Ç—É–∂–Ω–æ—Å—Ç—ñ –ö–æ–º–ø–∞–Ω—ñ—è –≤–∏—Ä–æ–±–ª—è—î —Ç—Ä–∏ –≤–∏–¥–∏ –ø—Ä–æ–¥—É–∫—Ü—ñ—ó: 
# –ï–Ω–µ—Ä–≥–µ—Ç–∏—á–Ω–∏–π –±–∞—Ç–æ–Ω—á–∏–∫ –ü—Ä–æ—Ç–µ—ó–Ω–æ–≤–∏–π —à–µ–π–∫ –í—ñ—Ç–∞–º—ñ–Ω–Ω–∏–π —Å–º—É–∑—ñ 
# üîπ –†–µ—Å—É—Ä—Å–∏ —Ç–∞ —ó—Ö –æ–±–º–µ–∂–µ–Ω–Ω—è –†–µ—Å—É—Ä—Å 
# –î–æ—Å—Ç—É–ø–Ω–æ –í–æ–¥–∞ 200 –æ–¥. 
# –°–∏—Ä–æ–≤–∞—Ç–∫–æ–≤–∏–π –ø—Ä–æ—Ç–µ—ó–Ω 60 –æ–¥. 
# –§—Ä—É–∫—Ç–æ–≤–∞ —Å—É–º—ñ—à 90 –æ–¥. 
# –í—ñ–≤—Å—è–Ω–∞ –æ—Å–Ω–æ–≤–∞ 80 –æ–¥. 
# üîπ –í–∏—Ç—Ä–∞—Ç–∏ —Ä–µ—Å—É—Ä—Å—ñ–≤ –Ω–∞ 1 –æ–¥–∏–Ω–∏—Ü—é –ø—Ä–æ–¥—É–∫—Ü—ñ—ó 
# üç´ –ï–Ω–µ—Ä–≥–µ—Ç–∏—á–Ω–∏–π –±–∞—Ç–æ–Ω—á–∏–∫ –í—ñ–≤—Å—è–Ω–∞ –æ—Å–Ω–æ–≤–∞ ‚Äî 4 –ü—Ä–æ—Ç–µ—ó–Ω ‚Äî 1 
# ü•§ –ü—Ä–æ—Ç–µ—ó–Ω–æ–≤–∏–π —à–µ–π–∫ –í–æ–¥–∞ ‚Äî 5 –ü—Ä–æ—Ç–µ—ó–Ω ‚Äî 2 
# üçì –í—ñ—Ç–∞–º—ñ–Ω–Ω–∏–π —Å–º—É–∑—ñ –í–æ–¥–∞ ‚Äî 4 –§—Ä—É–∫—Ç–æ–≤–∞ —Å—É–º—ñ—à ‚Äî 3 
# üîπ –î–æ–¥–∞—Ç–∫–æ–≤—ñ –æ–±–º–µ–∂–µ–Ω–Ω—è (–í–ê–ñ–õ–ò–í–û) –ó–∞–≥–∞–ª—å–Ω–∞ –∫—ñ–ª—å–∫—ñ—Å—Ç—å –Ω–∞–ø–æ—ó–≤ (—à–µ–π–∫–∏ + —Å–º—É–∑—ñ) –Ω–µ –º–æ–∂–µ –ø–µ—Ä–µ–≤–∏—â—É–≤–∞—Ç–∏ 40 –æ–¥–∏–Ω–∏—Ü—å (–æ–±–º–µ–∂–µ–Ω–Ω—è –æ–±–ª–∞–¥–Ω–∞–Ω–Ω—è). 
# –ö—ñ–ª—å–∫—ñ—Å—Ç—å –±–∞—Ç–æ–Ω—á–∏–∫—ñ–≤ –º–∞—î –±—É—Ç–∏ –Ω–µ –º–µ–Ω—à–æ—é, –Ω—ñ–∂ –∫—ñ–ª—å–∫—ñ—Å—Ç—å —Å–º—É–∑—ñ (–∫–æ–Ω—Ç—Ä–∞–∫—Ç–Ω–∞ –≤–∏–º–æ–≥–∞).

from pulp import LpProblem, LpVariable, LpMaximize, LpStatus, value

model = LpProblem("Production_Optimization", LpMaximize)

CB = LpVariable("Candy_Bars", lowBound=0, cat="Integer")
PS = LpVariable("Protein_Shakes", lowBound=0, cat="Integer")
VS = LpVariable("Vitamin_Smoothies", lowBound=0, cat="Integer")

# –†–µ—Å—É—Ä—Å–∏
model += 5 * PS + 4 * VS <= 200, "Water"
model += 1 * CB + 2 * PS <= 60, "Whey_Protein"
model += 3 * VS <= 90, "Fruit_Mix"
model += 4 * CB <= 80, "Oat_Base"

# –î–æ–¥–∞—Ç–∫–æ–≤—ñ —É–º–æ–≤–∏
model += PS + VS <= 40, "Max_Drinks"
model += CB >= VS, "Bars_ge_Smoothies"

# –¶—ñ–ª—å–æ–≤–∞ —Ñ—É–Ω–∫—Ü—ñ—è
model += CB + PS + VS, "Total_Products"

model.solve()

print("–°—Ç–∞—Ç—É—Å:", LpStatus[model.status])
print("–û–ø—Ç–∏–º–∞–ª—å–Ω–∏–π –ø–ª–∞–Ω –≤–∏—Ä–æ–±–Ω–∏—Ü—Ç–≤–∞:")
print("–ë–∞—Ç–æ–Ω—á–∏–∫–∏:", int(value(CB)))
print("–ü—Ä–æ—Ç–µ—ó–Ω–æ–≤—ñ –∫–æ–∫—Ç–µ–π–ª—ñ:", int(value(PS)))
print("–í—ñ—Ç–∞–º—ñ–Ω–Ω—ñ —Å–º—É–∑—ñ:", int(value(VS)))
print("–ó–∞–≥–∞–ª–æ–º:", int(value(CB + PS + VS)))

print("\n–í–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω–Ω—è —Ä–µ—Å—É—Ä—Å—ñ–≤:")
print("–í–æ–¥–∞:", int(value(5 * PS + 4 * VS)), "/ 200")
print("–°–∏—Ä–æ–≤–∞—Ç–∫–æ–≤–∏–π –ø—Ä–æ—Ç–µ—ó–Ω:", int(value(1 * CB + 2 * PS)), "/ 60")
print("–§—Ä—É–∫—Ç–æ–≤–∞ —Å—É–º—ñ—à:", int(value(3 * VS)), "/ 90")    
print("–í—ñ–≤—Å—è–Ω–∞ –æ—Å–Ω–æ–≤–∞:", int(value(4 * CB)), "/ 80")    

